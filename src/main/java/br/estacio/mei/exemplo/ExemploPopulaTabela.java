    public void addLinhaNaTabela(Aluno aluno) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //model.setRowCount(0);
        Object rowData[] = new Object[4];
        rowData[0] = aluno.getNome();
        rowData[1] = aluno.getRA();
        rowData[2] = aluno.getEquipe();
        rowData[3] = aluno.getNota();

        model.addRow(rowData);
    }

    public void populaTabela() {

        AlunoDao dao = AlunoDao.getInstance();
        ArrayList<Aluno> lista = dao.listarAlunos();

        for (int i = 0; i < lista.size(); i++) {
            addLinhaNaTabela(lista.get(i));
        }
        jTable1.repaint();

    }

